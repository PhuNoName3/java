    private void btlKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlKhachHangActionPerformed
        // TODO add your handling code here:
     String email = txtKH.getText();
    
    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập email.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        Connection connection = Connect.getConnection();

        String sql = "SELECT COUNT(*) FROM KhachHang WHERE Email = ?";
        PreparedStatement ps = connection.prepareStatement(sql);
        ps.setString(1, email);

        ResultSet rs = ps.executeQuery();
        rs.next();
        int count = rs.getInt(1);

        if (count > 0) {
            JOptionPane.showMessageDialog(this, "Email hợp lệ. Chuyển sang giao diện chính.");
            
            MainForm mainForm = new MainForm(); 
            mainForm.setVisible(true); 
        } else {
            JOptionPane.showMessageDialog(this, "Email không tồn tại. Vui lòng kiểm tra lại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        rs.close();
        ps.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Lỗi khi kiểm tra email: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btlKhachHangActionPerformed

