    private void btlDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlDangNhapActionPerformed
        // TODO add your handling code here:
  String taiKhoan = txtTaiKhoan.getText();
    String matKhau = txtMatKhau.getText();

    try {
        // Lấy kết nối (nếu chưa có thì tạo mới, nếu đã có thì dùng lại)
        Connection connection = Connect.getConnection();

        // Kiểm tra thông tin đăng nhập
        String sql = "SELECT COUNT(*) FROM QuanTriVien WHERE TenDangNhap = ? AND MatKhau = ?";
        PreparedStatement ps = connection.prepareStatement(sql);
        ps.setString(1, taiKhoan);
        ps.setString(2, matKhau);

        ResultSet rs = ps.executeQuery();
        rs.next();
        int count = rs.getInt(1);

        if (count > 0) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
            this.dispose();
            Main main = new Main(); 
            main.setVisible(true); 
        } else {
            JOptionPane.showMessageDialog(this, "Đăng nhập thất bại. Vui lòng kiểm tra lại thông tin đăng nhập.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        rs.close();
        ps.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Lỗi khi đăng nhập: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btlDangNhapActionPerformed

